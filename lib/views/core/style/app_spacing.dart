/// 8pt spacing scale from the design system (values in logical pixels).
class AppSpacing {
  AppSpacing._();

  /// 0.5rem — tight label/value
  static const double s2 = 8;

  /// 1rem — standard internal padding
  static const double s4 = 16;

  /// 1.5rem — gap between cards / sections
  static const double s6 = 24;

  /// 2.5rem — major section breaks
  static const double s10 = 40;

  /// Roundedness XL — cards, large controls (1.5rem)
  static const double radiusXl = 24;

  /// Pills / primary buttons
  static const double radiusFull = 999;
}
